# https://leetcode.com/problems/triangle/


class Solution:
    def minimumTotal(self, triangle: list[list[int]]) -> int:
        mins = triangle[-1]
        for row in reversed(triangle[:-1]):
            for i in range(len(row)):
                mins[i] = row[i] + min(mins[i], mins[i + 1])
        return mins[0]


